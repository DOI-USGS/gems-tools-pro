from GeMS_utilityFunctions import stringIsGeMSNull

def isWPGCMYKGcolor(color):
    if color == None:
        return False
    i = str(color)
    if stringIsGeMSNull(i) or i.isspace():
        return False
    i = int(color)
    if i >= 0 and i <= 999:
        return True
    else:
        return False

